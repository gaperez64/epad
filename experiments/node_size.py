class _Timeout(Exception):
    pass


def _alarm(signum, frame):
    raise _Timeout()


def _with_timeout(seconds, fn, *a, **k):
    prev = signal.signal(signal.SIGALRM, _alarm)
    signal.alarm(seconds)
    try:
        return fn(*a, **k)
    finally:
        signal.alarm(0)
        signal.signal(signal.SIGALRM, prev)


def existential_min_node_size(lds: LinDivs, *, check_sym_inc=True,
                              use_all_cx_inc=False, norm_timeout_s=20,
                              z3_timeout_ms=1000) -> dict:
    """Run KNF normalization and report the smallest L over *satisfiable*
    leaves -- the existential compact-stratum quantity.

    The smallest leaf is chosen by the size of a *reduced* (HNF) basis of L:
    the raw L accumulated by knf_norm may carry redundant large-coefficient
    generators, and the conjecture bounds the size of a minimal representation.

    Returns: status, n_leaves, n_sat, min (reduced l_metrics of the smallest
    sat leaf), min_raw (raw l_metrics of the same leaf), best_order, best_L.
    """
    try:
        with contextlib.redirect_stdout(io.StringIO()):
            leaves = _with_timeout(norm_timeout_s, lds.knf_norm,
                                   check_sym_inc=check_sym_inc,
                                   use_all_cx_inc=use_all_cx_inc)
    except _Timeout:
        return {"status": "norm_timeout"}
    except Exception as e:  # preprocessing limitations etc.
        return {"status": f"exc:{type(e).__name__}"}

    best = None
    n_sat = 0
    for lf in leaves:
        if leaf_is_sat(lf, z3_timeout_ms) is not True:
            continue
        n_sat += 1
        L = lf.L_gens if isinstance(lf, KNFLeaf) else ()
        m = l_metrics_lll(L)   # honest node size: shortest representation
        key = (m["total_bits"], m["ngen"])
        if best is None or key < best[0]:
            order = getattr(lf, "order", None)
            best = (key, m, order, L, l_metrics(L))

    if best is None:
        return {"status": "unsat_or_inconclusive",
                "n_leaves": len(leaves), "n_sat": 0}
    return {"status": "ok", "n_leaves": len(leaves), "n_sat": n_sat,
            "min": best[1], "min_raw": best[4],
            "best_order": best[2], "best_L": best[3]}


# --------------------------------------------------------------------------
# Fixed-order, solution-compatible driver (the Section 14 obstruction probe).
# --------------------------------------------------------------------------

def _model(F, G, xs, *, positive: bool, timeout_ms: int):
    s = _solver_for(F, G, (), xs, positive=positive, tag="mdl")
    s.set("timeout", timeout_ms)
    if s.check() != z3.sat:
        return None
    m = s.model()
    return [int(m.eval(x, model_completion=True).as_long()) for x in xs]


def _eval(vec, a) -> int:
    return sum(int(c) * a[i] for i, c in enumerate(vec[:-1])) + int(vec[-1])


def solution_compatible_L(lds: LinDivs, order, *, positive=True,
                          max_steps=64, z3_timeout_ms=2000,
                          model=None) -> dict:
    """Accumulate L for a *fixed* variable order along the branch a specific
    natural solution forces.

    Picks a nontrivial natural model `a` of Phi (x_i>=1 when `positive`), then
    repeatedly: take the first KNF non-increasingness witness (f, {g}) for the
    fixed `order`, and for each witness vector g add the repair equality
    `c*f - g = 0` with `c = g(a)/f(a)` -- the unique integer the solution `a`
    forces (well-defined since g in M~_f implies f(a)|g(a)).  Terminates when
    `order` is KNF-increasing (no witnesses), with depth <= d+1 by Lemma 3.2.

    Returns a dict: status, steps, L (the accumulated module), and l_metrics.
    """
    # Preprocess exactly as knf_norm does, then take the (single) subsystem
    # whose dimension matches `order`.  For sign-definite systems this is the
    # reduced input itself.
    with contextlib.redirect_stdout(io.StringIO()):
        subs = list(lds.all_disj_left_pos())
    sub = None
    for s in subs:
        if s and len(s.F[0]) - 1 == len(order):
            sub = s
            break
    if sub is None:
        return {"status": "no_matching_subsystem"}

    d = len(sub.F[0]) - 1
    if model is not None:
        # Caller-supplied solution (e.g. the chain's doubly-exponential
        # solution, which z3 cannot find once it grows large enough).
        assert len(model) == d, (len(model), d)
        a = list(model)
    else:
        xs = [z3.Int(f"x{i}") for i in range(d)]
        a = _model(sub.F, sub.G, xs, positive=positive,
                   timeout_ms=z3_timeout_ms)
        if a is None:
            return {"status": "no_nontrivial_model"}

    L = tuple()
    for step in range(max_steps):
        with contextlib.redirect_stdout(io.StringIO()):
            neqs = sub.all_non_increasing_knf(order, L)
        if not neqs:
            m = l_metrics(L)
            m.update({"status": "increasing", "steps": step, "L": L,
                      "model": a})
            return m
        f, Gw = neqs[0]
        fa = _eval(f, a)
        if fa == 0:
            return {"status": "degenerate_zero_lhs", "steps": step, "L": L}
        new_eqs = []
        for g in Gw:
            ga = _eval(g, a)
            if ga % fa != 0:
                return {"status": "non_integer_quotient", "steps": step,
                        "f": f, "g": g, "fa": fa, "ga": ga}
            c = ga // fa
            new_eqs.append(tuple(c * f[i] - g[i] for i in range(len(f))))
        cand = L + tuple(new_eqs)
        # Lemma 3.2: a genuine repair strictly grows the module L.  With the
        # Z-module-membership witness test every witness is genuine, so this
        # always holds and the loop terminates at "increasing".  This guard is
        # a defensive net: if some repair failed to grow the rank (e.g. a
        # residual spurious witness) we stop with a clear status instead of
        # looping.  Note: a *bad fixed order* still terminates here, but only
        # after L accumulates doubly-exponentially large coefficients -- that
        # blow-up, not non-termination, is the Section 14 obstruction.
        if _rank(cand) <= _rank(L):
            m = l_metrics(L)
            m.update({"status": "rank_stalled", "steps": step,
                      "rank": _rank(L), "max_rank": len(order) + 1,
                      "n_witness_vecs": sum(len(Gw) for _, Gw in neqs),
                      "L": L})
            return m
        L = cand
    return {"status": "max_steps_exceeded", "steps": max_steps, "L": L}


from itertools import permutations, combinations, product as _product  # noqa: E402


def _is_increasing(sub: LinDivs, order, L_gens) -> bool:
    with contextlib.redirect_stdout(io.StringIO()):
        return len(sub.all_non_increasing_knf(order, L_gens)) == 0


def _phi_and_L_sat(sub: LinDivs, L_gens, z3_timeout_ms=500) -> Optional[bool]:
    d = len(sub.F[0]) - 1
    xs = [z3.Int(f"x{i}") for i in range(d)]
    s = _solver_for(sub.F, sub.G, L_gens, xs, positive=False, tag="cert")
    s.set("timeout", z3_timeout_ms)
    r = s.check()
    return True if r == z3.sat else (False if r == z3.unsat else None)


def _candidate_relations(d, max_coef):
    """Primitive (d+1)-vectors with |coef| <= max_coef, nonzero linear part,
    in canonical sign (first nonzero coefficient positive)."""
    import math as _m
    rng = range(-max_coef, max_coef + 1)
    seen = set()
    out = []
    for tail in _product(rng, repeat=d + 1):
        if not any(tail[:d]):           # need a nonzero variable coefficient
            continue
        g = 0
        for c in tail:
            g = _m.gcd(g, c)
        if g != 1:                       # primitive only
            continue
        # canonical sign: first nonzero positive
        fnz = next(c for c in tail if c != 0)
        v = tail if fnz > 0 else tuple(-c for c in tail)
        if v not in seen:
            seen.add(v)
            out.append(v)
    return out


def smallest_certificate(lds: LinDivs, *, max_coef=2, max_gens=3,
                         z3_timeout_ms=500):
    """Search for the SMALLEST equality module L (any low-coefficient L, not
    just the ones knf_norm happens to generate) such that some order is
    quotient-increasing for (Phi, L) AND Phi ∧ L is satisfiable.

    This is the honest compact-stratum quantity of Problem 16.1: existence of
    *any* small L, over all orders and all solution regions.  knf_norm's L is
    only an upper bound (e.g. it reports x=N*y for cross(N) while x=y works);
    this search finds the true minimum within the coefficient/size budget.

    Requires `lds` to be a pure-divisibility, sign-definite system (no eqs or
    ineqs) so that `lds` itself is the increasingness subsystem.

    Returns: status ('ok'/'none_within_budget'), L, ngen, l_metrics of the
    smallest certificate, and the witnessing order.
    """
    assert not lds.get_eqs() and not lds.get_ineqs()
    d = len(lds.F[0]) - 1
    orders = list(permutations(range(d)))

    # k = 0: is Phi already increasing for some order with empty L?
    if _phi_and_L_sat(lds, (), z3_timeout_ms):
        for order in orders:
            if _is_increasing(lds, order, ()):
                return {"status": "ok", "L": (), "ngen": 0,
                        "metrics": l_metrics(()), "order": order}

    cands = _candidate_relations(d, max_coef)
    for k in range(1, max_gens + 1):
        best = None
        for combo in combinations(cands, k):
            L = tuple(combo)
            if _phi_and_L_sat(lds, L, z3_timeout_ms) is not True:
                continue
            ok_order = None
            for order in orders:
                if _is_increasing(lds, order, L):
                    ok_order = order
                    break
            if ok_order is None:
                continue
            m = l_metrics_lll(L)
            key = (m["total_bits"], m["max_abs"])
            if best is None or key < best[0]:
                best = (key, L, m, ok_order)
        if best is not None:
            return {"status": "ok", "L": best[1], "ngen": k,
                    "metrics": best[2], "order": best[3]}
    return {"status": "none_within_budget",
            "max_coef": max_coef, "max_gens": max_gens}


# --------------------------------------------------------------------------
# Repair-mechanism classification.
#
# Conjecture (four-mechanism repair completeness): every satisfiable non-KNF
# kernel state (Phi, L, chi) has a poly-size satisfiability-preserving repair
# r not in L produced by one of:
#   (1) acyclic scheduling / filtered DAG acceptance  -- a good order exists
#       at the current L, so LV(f) is *defined* by lower variables (the repair
#       is a reorder; no new relation is forced);
#   (2) homogeneous zero collapse                     -- r = (x_i = 0);
#   (3) local cycle / cross-component relation         -- r = p_i = c*p_j
#       (an affine 2-component relation, constant allowed by positive branch);
#   (4) finite-gcd pin f = d from lower cancellation f|C -- r = (c*x_i = d).
#
# `classify_repair_generator` labels a single reduced relation; a repair with
# >= 3 nonzero variable coefficients is a candidate FIFTH mechanism (none has
# been found: such "definitions" of LV(f) by lower variables are absorbed by
# mechanism 1 -- they make some order increasing, so L comes back empty).
# --------------------------------------------------------------------------

def classify_repair_generator(v) -> str:
    """Classify one (reduced) repair relation vector against the four
    mechanisms; return 'FIFTH(nvars=k)' for a genuinely >=3-variable relation
    that fits none of them."""
    nvar = sum(1 for c in v[:-1] if c != 0)
    has_const = v[-1] != 0
    if nvar == 0:
        return "trivial"                       # pure-constant (degenerate)
    if nvar == 1 and not has_const:
        return "M2:zero-collapse"              # x_i = 0
    if nvar == 1 and has_const:
        return "M4:gcd-pin"                    # c*x_i = d  ->  x_i = d/c
    if nvar == 2:
        return "M3:2-comp" + ("(affine)" if has_const else "")
    return f"FIFTH(nvars={nvar})"


def classify_node_repair(lds: LinDivs, *, norm_timeout_s=10,
                         z3_timeout_ms=500) -> dict:
    """Run the existential measurement and classify the minimal repair of the
    resulting kernel state against the four mechanisms.

    Returns: status, classes (list of per-generator labels), and `fifth`
    (True if any generator is a genuine >=3-variable relation outside the
    taxonomy).  An empty stratum is mechanism 1 (acyclic reschedule)."""
    r = existential_min_node_size(lds, norm_timeout_s=norm_timeout_s,
                                  z3_timeout_ms=z3_timeout_ms)
    if r["status"] != "ok":
        return {"status": r["status"], "classes": None, "fifth": False}
    L = reduce_L_lll(r["best_L"])
    if not L:
        return {"status": "ok", "classes": ["M1:reschedule"], "fifth": False}
    classes = [classify_repair_generator(v) for v in L]
    return {"status": "ok", "classes": classes,
            "fifth": any(c.startswith("FIFTH") for c in classes)}


def min_node_size_over_orders(lds: LinDivs, solution, *, max_orders=5040,
                              metric="max_bits") -> dict:
    """The rigorous existential node-size of a *sign-definite* satisfiable
    system: over EVERY variable order, compute the fixed-order stratum L that
    the given `solution` forces (`solution_compatible_L`), and take the
    minimum.  This is the quantity Conjecture 12.3 / Problem 16.1 bounds:
    `min_chi  bitsize( minimal quotient-increasing L for order chi )`.

    A super-polynomial growth of this minimum in the input size is a
    counterexample to the compact-stratum conjecture.  (`max_bits` = bit-length
    of the largest |coefficient|; `total_bits` = summed binary size.)

    Requires `lds` sign-definite (so `all_disj_left_pos` is a no-op and the
    given `solution` lives in the unique subsystem)."""
    d = len(lds.F[0]) - 1
    best = None
    per_order = []
    n_orders = 0
    for order in permutations(range(d)):
        n_orders += 1
        if n_orders > max_orders:
            break
        r = solution_compatible_L(lds, order, model=solution)
        if r.get("status") != "increasing":
            per_order.append((order, r.get("status"), None))
            continue
        red = l_metrics_lll(r["L"])   # honest node size (shortest basis)
        val = red[metric]
        per_order.append((order, "increasing", val))
        if best is None or val < best[0]:
            best = (val, order, red)
    if best is None:
        return {"status": "no_increasing_order", "n_orders": n_orders,
                "per_order": per_order}
    incr_vals = [v for _, st, v in per_order if st == "increasing"]
    return {"status": "ok", "n_orders": n_orders,
            "min_metric": best[0], "min_order": best[1], "min_L": best[2],
            "max_metric": max(incr_vals), "n_increasing": len(incr_vals),
            "per_order": per_order}


