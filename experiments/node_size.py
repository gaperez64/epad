def chain(n: int) -> LinDivs:
    """Lipshitz/EPAD divisibility chain over x_0,...,x_n:

        x_i | x_{i+1}  and  (x_i + 1) | x_{i+1}   for i = 0,...,n-1.

    gcd(x_i, x_i+1)=1 forces x_i(x_i+1) | x_{i+1}, so x_{i+1} >= x_i^2 and the
    least nontrivial solution x_0=1, x_{i+1}=x_i(x_i+1) is doubly exponential.
    Increasing in the forward order x_0 < ... < x_n (L empty).
    """
    veclen = (n + 1) + 1

    def vec(pairs, const=0):
        v = [0] * veclen
        for i, c in pairs:
            v[i] = c
        v[-1] = const
        return tuple(v)

    F, G = [], []
    for i in range(n):
        F.append(vec([(i, 1)]))
        G.append(vec([(i + 1, 1)]))
        F.append(vec([(i, 1)], const=1))
        G.append(vec([(i + 1, 1)]))
    return LinDivs(tuple(F), tuple(G))


def forward_order(n: int):
    """x_0 < x_1 < ... < x_n -- the good order for chain(n)."""
    return tuple(range(n + 1))


def reverse_order(n: int):
    """x_n < ... < x_0 -- the Section 14 'bad fixed order' for chain(n)."""
    return tuple(reversed(range(n + 1)))


def chain_solution(n: int):
    """The least nontrivial natural solution of chain(n): x_0 = 1,
    x_{i+1} = x_i*(x_i + 1).  Doubly exponential, so z3 cannot rediscover it
    for large n -- pass it to solution_compatible_L(..., model=chain_solution(n))
    to drive the Section 14 blow-up past the point z3 can find a model."""
    a = [1]
    for _ in range(n):
        a.append(a[-1] * (a[-1] + 1))
    return tuple(a)


def phi_forces_value(N: int) -> LinDivs:
    """Barros' gadget  (x | y) and (x | y+N) and (N | x), which forces x = N.

    x|y and x|(y+N) give x|N, and N|x gives the reverse, so the positive
    branch pins x = N (the smallest nontrivial solution is (x,y)=(N,0)).

    It defeats TWO of the compactness escapes simultaneously:
      * no good order with empty L -- x|N puts the constant N in M_x, so x is
        non-increasing for every order;
      * no all-zero collapse -- x=0 is infeasible (0 does not divide N).
    The only remaining escape is the small-solution pin {x = N}, an
    LLL-irreducible relation of size bit-length(N).  Since N is an input
    constant the node size is O(input size) -- compact, not a counterexample,
    but the cleanest illustration of the binding escape.

    Note: contains the constant (variable-free) divisor N|x, exercising the
    constant-divisor handling in the increasingness check."""
    return LinDivs(divisors=((1, 0, 0), (1, 0, 0), (0, 0, N)),
                   dividends=((0, 1, 0), (0, 1, N), (1, 0, 0)))


def doubling_chain(n: int) -> LinDivs:
    """x_{i+1} = 2*x_i exactly, via 2*x_i | x_{i+1} and x_{i+1} | 2*x_i, with
    x_0 >= 1 forced.  Solutions grow as x_i = 2^i*x_0 (single exponential), yet
    the compact stratum is the n LOCAL relations x_{i+1} - 2*x_i = 0 (all
    coefficients 2): the stratum encodes RELATIONS, not absolute VALUES.

    A cautionary family for the hunt: its HNF echelon basis composes the
    relations into x_n = 2^n*x_0 (coefficient 2^n), so an HNF-based node size
    would look exponential -- but the LLL short basis is O(1) per relation, so
    the honest node size is O(n).  Compact, consistent with the conjecture."""
    veclen = (n + 1) + 1

    def vec(pairs):
        v = [0] * veclen
        for i, c in pairs:
            v[i] = c
        return tuple(v)

    F, G = [], []
    for i in range(n):
        F.append(vec([(i, 2)]))      # 2*x_i | x_{i+1}
        G.append(vec([(i + 1, 1)]))
        F.append(vec([(i + 1, 1)]))  # x_{i+1} | 2*x_i  -> together x_{i+1}=2x_i
        G.append(vec([(i, 2)]))
    ineq = (tuple([-1] + [0] * n + [1]),)  # x_0 >= 1
    return LinDivs(tuple(F), tuple(G), ineqconstrs=ineq)


def cross(N: int) -> LinDivs:
    """The cross gadget  x | N*y  and  y | N*x.

    The cleanest "every order needs a non-empty, lattice-irreducible L" family:
    with the ratio-N solution (x,y)=(N,1) BOTH orders force the single relation
    x = N*y (a primitive vector, so LLL cannot shorten it).  The node size is
    thus exactly bit-length(N) for every order -- non-empty and lattice-forced,
    yet still COMPACT because N is an input coefficient (size log N).  It does
    not amplify: the forced coefficient equals the input coefficient."""
    return LinDivs(((1, 0, 0), (0, 1, 0)), ((0, N, 0), (N, 0, 0)))


def cross_chain(n: int) -> LinDivs:
    """A chain of factor-2 cross gadgets: x_i | 2 x_{i+1} and x_{i+1} | 2 x_i.

    Solution x_i = 2^i (ratios of 2).  An attempt to *compound* the cross
    gadget into an exponential node size -- but it fails to amplify: every
    order can use the LOCAL coefficient-2 relations, so min-over-orders node
    size stays O(log n) and even the worst order grows only linearly (no
    doubly-exponential composition, unlike `chain`, because the coupling is
    additive/factor-2 rather than squaring).  Compact."""
    veclen = (n + 1) + 1

    def vec(pairs):
        v = [0] * veclen
        for i, c in pairs:
            v[i] = c
        return tuple(v)

    F, G = [], []
    for i in range(n):
        F.append(vec([(i, 1)]))
        G.append(vec([(i + 1, 2)]))
        F.append(vec([(i + 1, 1)]))
        G.append(vec([(i, 2)]))
    return LinDivs(tuple(F), tuple(G))


def all_equal(n: int) -> LinDivs:
    """A sign-definite family forcing x_0 = x_1 = ... = x_{n-1} via a
    divisibility cycle x_i | x_{i+1 mod n} (Alessio's example generalised).
    Satisfiable (all-equal assignments) and increasing; a control with more
    than one divisor per variable but still empty existential L."""
    def vec(i):
        v = [0] * (n + 1)
        v[i] = 1
        return tuple(v)
    F, G = [], []
    for i in range(n):
        F.append(vec(i))
        G.append(vec((i + 1) % n))
    return LinDivs(tuple(F), tuple(G))


def antonia(n: int) -> LinDivs:
    """Antonia-style nested chain over x_0,...,x_n:

        x_i, (x_i + 1) | x_{i+1}   with   x_i >= 2 enforced as -x_i + 2 <= 0.

    This is the sign-definite, satisfiable companion to `chain` that exercises
    the inequality-elimination path of preprocessing.  Generalises the n=2
    `test_antonias_paper_example`."""
    veclen = (n + 1) + 1

    def vec(pairs, const=0):
        v = [0] * veclen
        for i, c in pairs:
            v[i] = c
        v[-1] = const
        return tuple(v)

    F, G = [], []
    for i in range(n):
        F.append(vec([(i, 1)]))
        G.append(vec([(i + 1, 1)]))
        F.append(vec([(i, 1)], const=1))
        G.append(vec([(i + 1, 1)]))
    ineqs = tuple(vec([(i, -1)], const=2) for i in range(n + 1))
    return LinDivs(tuple(F), tuple(G), ineqconstrs=ineqs)


