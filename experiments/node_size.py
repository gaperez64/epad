"""Node-representation-size measurement for the compact-stratum conjectures.

The KNF normalizer (`LinDivs.knf_norm`) accumulates the quotient-increasing
equality module `L` (the "kernel state") at every node.  The companion note
*Kernel Normal Form, Flat Presentations, and Automata/Arithmetic-Circuit
Compactness for EPAD* leaves open whether, for every satisfiable Phi, there is
a *small* such `L`:

  * Conjecture 12.3 / Problem 16.2 -- a quotient-increasing `L` with poly-size
    EDB-SLP generators always exists;
  * Problem 16.1 -- a poly-binary-size `L` always exists;
  * Problem 16.3 -- nonzero classes in
    `M~_f(Phi,L) cap R_<=k / (Zf+L) cap R_<=k` are representable compactly
    along *some* satisfiable solution-compatible branch.

The coefficient bit-size of `leaf.L_gens` *is* the node-representation size
those statements are about.  This module measures it two ways:

  1. `existential_min_node_size` -- the quantity the *existential* conjecture
     is about: run `knf_norm` (which is free to pick a good variable order),
     keep only satisfiable leaves, and report the smallest `L`.  A
     super-polynomial trend here would be a counterexample candidate.

  2. `solution_compatible_L` -- a *fixed-order* driver used to expose the
     Section 14 "fixed bad order" obstruction: pick a nontrivial natural
     solution `a` of Phi, fix a variable order, and let the KNF witnesses
     dictate the repair coefficients `c = g(a)/f(a)` that `a` forces.  This
     reproduces the doubly-exponential `lambda` in `x_n - lambda*x_{n-1}` that
     a bad order forces, while the existential measurement stays small.

Both are SAT-aware via z3, so they only ever report sizes of genuinely
solution-compatible branches.
"""

from __future__ import annotations

import contextlib
import io
import signal
from typing import Optional

import numpy as np
import z3

from systems.lindivs import LinDivs, KNFLeaf


def _rank(L_gens) -> int:
    """Rank over Q of the module generated by L_gens (rows are (d+1)-vectors,
    constant included so it can reach d+1)."""
    if not L_gens:
        return 0
    return int(np.linalg.matrix_rank(np.array(L_gens, dtype=float)))


# --------------------------------------------------------------------------
# bit-size metrics on an equality module L (a tuple of (d+1)-vectors)
# --------------------------------------------------------------------------

def l_metrics(L_gens) -> dict:
    """Coefficient-size summary of an equality module L.

    * ngen      -- number of generators
    * max_abs   -- largest |coefficient| over all generators
    * total_bits-- sum of bit-lengths of all coefficients (binary size of L)
    * max_bits  -- bit-length of the largest coefficient
    """
    coeffs = [c for l in L_gens for c in l]
    max_abs = max((abs(c) for c in coeffs), default=0)
    total_bits = sum(int(abs(c)).bit_length() for c in coeffs)
    return {
        "ngen": len(L_gens),
        "max_abs": max_abs,
        "max_bits": int(max_abs).bit_length(),
        "total_bits": total_bits,
    }


def reduce_L(L_gens):
    """Reduced (HNF) generating set of the Z-module L, dropping redundant
    generators.  The raw L accumulated by knf_norm can carry redundant
    large-coefficient generators (e.g. {x=0, 5x=0}); the *node-representation
    size* the conjecture bounds is the size of a reduced basis, so measure
    against this, not the raw generators."""
    from utils.matutils import column_style_hnf, transpose
    if not L_gens:
        return tuple()
    H, _ = column_style_hnf(transpose(tuple(L_gens)))
    return tuple(col for col in transpose(H) if any(c != 0 for c in col))


def l_metrics_reduced(L_gens) -> dict:
    """l_metrics of the HNF-reduced module (canonical, but may inflate
    coefficients -- see reduce_L_lll for the short-basis version)."""
    return l_metrics(reduce_L(L_gens))


def reduce_L_lll(L_gens):
    """Shortest-basis representation of the Z-module L, via LLL.

    HNF is canonical but echelon form composes relations and can INFLATE
    coefficients (e.g. the local relations x_{i+1}=2x_i, all coeff 2, have an
    HNF basis containing x_n=2^n x_0).  The compact-stratum conjecture asks
    whether *some* small generating set exists, so the honest node size is a
    short (LLL-reduced) basis, not the HNF.  We HNF first to drop redundant
    generators / extract a basis, then LLL that basis for short vectors."""
    import flint
    if not L_gens:
        return tuple()
    rows = [list(int(c) for c in g) for g in L_gens]
    H = flint.fmpz_mat(rows).hnf()
    basis = [r for r in H.tolist() if any(c != 0 for c in r)]
    if not basis:
        return tuple()
    red = flint.fmpz_mat(basis).lll()
    return tuple(tuple(int(c) for c in r) for r in red.tolist()
                 if any(c != 0 for c in r))


def l_metrics_lll(L_gens) -> dict:
    """l_metrics of the LLL short-basis of L -- the honest node size: the
    smallest generating-set representation the tool can find."""
    return l_metrics(reduce_L_lll(L_gens))


# --------------------------------------------------------------------------
# z3 helpers (kept independent of the test harness so this module stands
# alone, but deliberately matching tests/test_norm_xval.py semantics)
# --------------------------------------------------------------------------

def _aff(coeffs, xs):
    if not coeffs:
        return z3.IntVal(0)
    expr = z3.IntVal(int(coeffs[-1]))
    for i, c in enumerate(coeffs[:-1]):
        if c != 0:
            expr = expr + int(c) * xs[i]
    return expr


def _dim(*mats) -> int:
    for m in mats:
        if m:
            return len(m[0]) - 1
    return 0


def _solver_for(F, G, L_gens, xs, *, positive: bool, tag: str):
    """A z3 solver asserting f_i|g_i (via fresh k_i), L equalities, and
    x_i>=0 (or x_i>=1 when `positive`)."""
    s = z3.Solver()
    for i, x in enumerate(xs):
        s.add(x >= (1 if positive else 0))
    for i, (f, g) in enumerate(zip(F, G)):
        k = z3.Int(f"k_{tag}_{i}")
        s.add(_aff(f, xs) * k == _aff(g, xs))
    for l in (L_gens or ()):
        s.add(_aff(l, xs) == 0)
    return s


def leaf_is_sat(leaf, timeout_ms: int = 1000) -> Optional[bool]:
    """True/False/None(unknown).  Variables are non-negative (EPAD
    reparametrises solutions to the naturals)."""
    if isinstance(leaf, KNFLeaf):
        F, G, L = leaf.F, leaf.G, leaf.L_gens
    elif isinstance(leaf, LinDivs):
        if not leaf.F:
            return True  # trivially sat empty system
        F, G, L = leaf.F, leaf.G, ()
    else:  # OrdLinDivs
        F, G, L = leaf.F, leaf.G, ()
    if not F:
        return True
    d = _dim(F, G, L)
    xs = [z3.Int(f"x{i}") for i in range(d)]
    s = _solver_for(F, G, L, xs, positive=False, tag=f"lf{id(leaf)}")
    s.set("timeout", timeout_ms)
    r = s.check()
    return True if r == z3.sat else (False if r == z3.unsat else None)


